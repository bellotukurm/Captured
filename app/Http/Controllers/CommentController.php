    public function apiIndex(Post $post)
    {
        $comments = Comment::whereIn('post_id',[$post->id])->get();
        return $comments;
    }

    public function apiStore(Request $request, Post $post, User $user)
    {
        $comment = new Comment;
        $comment->text = $request['text'];
        
        $comment->post_id = $post->id;
        $comment->user_id = $user->id;
        $comment->username = $user->username;
        $comment->save();
        return $comment;
    }
